/* USER CODE BEGIN 4 */
void Execute_CMD(void)
{
	const char s[2] = " ";
	char *val[8];
	char *p = strtok((char*)RxBuff, s);
	uint8_t i = 0;
	while (p != NULL)
	{
		val[i++] = p;
		p = strtok(NULL, s);
	}

	if (strcmp(val[0], "ROT") == 0)
	{
		servo_t tmp = (strcmp(val[1], "S1") == 0) ? S1 : S2;
		Servo_SetAngle(&tmp, atoi(val[2]));
	}

	if (strcmp(val[0], "CAL") == 0)
	{
		servo_t tmp = (strcmp(val[1], "S1") == 0) ? S1 : S2;
		Servo_Calibrate(&tmp, atoi(val[2]));
	}
}

